#include "ft_printf.h"

int	ft_u(unsigned int u, int ret)
{
	if (u < 10)
		ret = ft_c(u + '0', ret);
	else
	{
		ret = ft_u(u / 10, ret);
		ret = ft_u(u % 10, ret);
	}
	return (ret);
}
